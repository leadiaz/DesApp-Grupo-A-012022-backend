package ar.edu.unq.desaapp.grupo.a.backenddesaappapi.model;

public class HourlyCryptoAssetData {

    private CryptoAssetsEnum cryptoAsset;
    private String day;
    private String time;
    private float price;


    public HourlyCryptoAssetData(CryptoAssetsEnum cryptoAsset, String day, String time, float price) {
        this.cryptoAsset = cryptoAsset;
        this.day = day;
        this.time = time;
        this.price = price;
    }

    public CryptoAssetsEnum getCryptoAsset() {
        return cryptoAsset;
    }

    public void setCryptoAsset(CryptoAssetsEnum cryptoAsset) {
        this.cryptoAsset = cryptoAsset;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
